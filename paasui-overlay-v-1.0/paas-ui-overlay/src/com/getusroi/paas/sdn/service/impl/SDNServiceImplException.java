package com.getusroi.paas.sdn.service.impl;

public class SDNServiceImplException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 894246251676926118L;

	public SDNServiceImplException() {
		super();
	}

	public SDNServiceImplException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SDNServiceImplException(String message, Throwable cause) {
		super(message, cause);
	}

	public SDNServiceImplException(String message) {
		super(message);
	}

	public SDNServiceImplException(Throwable cause) {
		super(cause);
	}
	
	

}
