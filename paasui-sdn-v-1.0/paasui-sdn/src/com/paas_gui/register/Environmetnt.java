package com.paas_gui.register;

public class Environmetnt {

	private String name;
	private String type;
	private String application;
	private String version;
	private String acceptTag;
	private String promoteTag;
	private String action;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getApplication() {
		return application;
	}
	public void setApplication(String application) {
		this.application = application;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getAcceptTag() {
		return acceptTag;
	}
	public void setAcceptTag(String acceptTag) {
		this.acceptTag = acceptTag;
	}
	public String getPromoteTag() {
		return promoteTag;
	}
	public void setPromoteTag(String promoteTag) {
		this.promoteTag = promoteTag;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	
	
	
}
